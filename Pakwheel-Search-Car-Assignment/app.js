// -------------------------------Display All Cars When Load In The Browsers----------------------------------
window.onload = DisplayCards()
function DisplayCards() {
    var OverallCard = document.getElementById('Overallcards')
    for (const company in cars) {
        if (typeof cars[company] === 'object') {
            for (const carname in cars[company]) {

                var HTMLCompany = document.getElementById('Company').innerHTML = company + ' <small id="carname" > </small> ' //
                var CarName = document.getElementById("Company").childNodes[1]
                CarName.innerHTML = carname.replaceAll("_", " ")
                var CArdetails = document.getElementById('cardetails')
                let Image = document.getElementById('image') 
                if (typeof cars[company][carname] === 'object') {
                    for (const cardetails in cars[company][carname]) {
                        if (cardetails.toLowerCase() == "image") {   
                            console.log(Image.src = cars[company][carname][cardetails])
                        } else {
                            CArdetails.innerHTML += `
    <div> <span> ${cardetails.replaceAll("_", " ")} : </span> <span>${cars[company][carname][cardetails]} </span> </div>
    `
                        }
                    }
                    CArdetails.id = '';
                    for (let index = 0; index < document.getElementsByTagName('h2').length; index++) {
                        document.getElementsByTagName('h2')[index].id = '';
                    }
                    CarName.id = '';
                    var last = document.getElementById('remove').id = ''
                    Image.id = '' 
                }
                CreateTemplete();
            }
        }
    }
    document.getElementById('remove').remove();

}

